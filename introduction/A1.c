#include<stdio.h>
#include<math.h>

int main()
{
	printf("%d\n", 11111 * 11111);
	printf("%d\n", 111111 * 111111);
	printf("%d\n", 111111111 * 111111111);

	printf("%f\n", 11111.0 * 11111);
	printf("%f\n", 111111.0 * 111111);
	printf("%f\n", 111111111.0 * 111111111);

	printf("%f\n", sqrt(-1 * 10));


	printf("%f\n", 1.0/0.0);
	printf("%f\n", 0.0/0.0);

	printf("%d\n", 0/0);
	return 0;

}

